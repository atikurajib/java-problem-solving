// Calculate the Area of a Rectangle

public class Main {
	public static void main(String[] args) {
    	int length = 10;
        int width = 15;
        int area;
        
        area = length * width;
        
        System.out.println("Length: " + length);
        System.out.println("Width: " + width);
        System.out.println("Area of Rectangle: " + area);
    }
}

